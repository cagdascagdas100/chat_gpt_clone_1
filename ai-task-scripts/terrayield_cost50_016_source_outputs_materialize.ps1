$ErrorActionPreference = 'Continue'

$TaskId = 'cost50-016-source-outputs-materialize-20260512'
$BridgeRoot = Split-Path -Parent $PSScriptRoot
$ConfigPath = Join-Path $BridgeRoot 'AAYS_TASK_BRIDGE_CONFIG.ps1'
if (Test-Path $ConfigPath) { . $ConfigPath }
$ProjectRoot = if ($env:AAYS_PROJECT_ROOT) { $env:AAYS_PROJECT_ROOT } else { 'E:\AAYS_DATA\cost\handoff_zips\cost_uk_postgres_50step_handoff_20260511_213229\terrayield_land_intelligence' }
$CostRoot = if ($env:AAYS_COST_DATA_ROOT) { $env:AAYS_COST_DATA_ROOT } else { 'E:\AAYS_DATA\cost' }
$SourcesDir = Join-Path $CostRoot 'sources'
$ResultDir = Join-Path $BridgeRoot 'ai-results'
New-Item -ItemType Directory -Force -Path $ResultDir | Out-Null
New-Item -ItemType Directory -Force -Path $SourcesDir | Out-Null

function Log([string]$Text) { Write-Output ('[' + (Get-Date -Format 's') + '] ' + $Text) }
function SafeWriteLines([string[]]$Lines, [string]$Path) {
  $tmp = $Path + '.tmp'
  $Lines | Set-Content -Encoding UTF8 -Path $tmp
  Move-Item -Force -LiteralPath $tmp -Destination $Path
}
function SafeWriteText([string]$Text, [string]$Path) {
  $tmp = $Path + '.tmp'
  Set-Content -Encoding UTF8 -Path $tmp -Value $Text
  Move-Item -Force -LiteralPath $tmp -Destination $Path
}
function CsvEscape([object]$Value) {
  if ($null -eq $Value) { return '' }
  $s = [string]$Value
  if ($s -match '[,"`r`n]') { return '"' + ($s -replace '"','""') + '"' }
  return $s
}
function ScoreRow($row) {
  $score = 50
  $sourceText = (($row.PSObject.Properties | ForEach-Object { [string]$_.Value }) -join ' ')
  if ($sourceText -match 'gov\.uk|ONS|DBT|HMRC|HMLR|official|government') { $score += 30 }
  if ($sourceText -match 'paid|professional|licensed|subscription') { $score += 10 }
  if ($sourceText -match 'seed|fallback|demo|sample') { $score -= 20 }
  if ($sourceText -match 'http://|https://') { $score += 5 }
  if ($score -lt 0) { $score = 0 }
  if ($score -gt 100) { $score = 100 }
  return [int]$score
}

Log "TASK=$TaskId"
Log 'MODE=source_outputs_materialize_safe'
Log "PROJECT_ROOT=$ProjectRoot"
Log "COST_ROOT=$CostRoot"
Log "SOURCES_DIR=$SourcesDir"

$latestJson = Join-Path $SourcesDir 'source_fetch_manifest_latest.json'
$latestCsv = Join-Path $SourcesDir 'source_fetch_manifest_latest.csv'
$scoredCsv = Join-Path $SourcesDir 'source_facts_scored.csv'
$summaryJson = Join-Path $SourcesDir 'source_facts_scored_summary.json'

$manifestJson = Get-ChildItem -Path $SourcesDir -Filter 'source_fetch_manifest_*.json' -File -ErrorAction SilentlyContinue |
  Where-Object { $_.Name -ne 'source_fetch_manifest_latest.json' } |
  Sort-Object LastWriteTime -Descending |
  Select-Object -First 1

if ($manifestJson) {
  Copy-Item -LiteralPath $manifestJson.FullName -Destination $latestJson -Force
  Log "LATEST_JSON_SOURCE=$($manifestJson.FullName)"
} elseif (-not (Test-Path $latestJson)) {
  SafeWriteText '{"sources":[],"generated_by":"cost50-016","note":"no prior manifest json found"}' $latestJson
  Log 'LATEST_JSON_CREATED_EMPTY=True'
}

$manifestCsv = Get-ChildItem -Path $SourcesDir -Filter 'source_fetch_manifest_*.csv' -File -ErrorAction SilentlyContinue |
  Where-Object { $_.Name -ne 'source_fetch_manifest_latest.csv' } |
  Sort-Object LastWriteTime -Descending |
  Select-Object -First 1

if ($manifestCsv) {
  Copy-Item -LiteralPath $manifestCsv.FullName -Destination $latestCsv -Force
  Log "LATEST_CSV_SOURCE=$($manifestCsv.FullName)"
} elseif (-not (Test-Path $latestCsv)) {
  SafeWriteLines @('source_id,source_url,fetched_at,source_type,confidence_policy','unknown,,,' + (Get-Date -Format s) + ',seed_fallback,generated_placeholder') $latestCsv
  Log 'LATEST_CSV_CREATED_PLACEHOLDER=True'
}

$extracted = Get-ChildItem -Path $SourcesDir -Filter 'source_facts_extracted_*.csv' -File -ErrorAction SilentlyContinue |
  Sort-Object LastWriteTime -Descending |
  Select-Object -First 1

$rows = @()
if ($extracted) {
  Log "EXTRACTED_SOURCE=$($extracted.FullName)"
  try { $rows = @(Import-Csv -LiteralPath $extracted.FullName) } catch { Log ('EXTRACTED_IMPORT_ERROR=' + $_.Exception.Message) }
} else {
  Log 'EXTRACTED_SOURCE_NOT_FOUND=True'
}

$outRows = @()
if ($rows.Count -gt 0) {
  foreach ($r in $rows) {
    $score = ScoreRow $r
    $tier = if ($score -ge 80) { 'high' } elseif ($score -ge 60) { 'medium' } else { 'low' }
    $obj = [ordered]@{}
    foreach ($p in $r.PSObject.Properties) { $obj[$p.Name] = $p.Value }
    $obj['source_reliability_score'] = $score
    $obj['source_reliability_tier'] = $tier
    $obj['scored_at'] = (Get-Date -Format s)
    $outRows += New-Object psobject -Property $obj
  }
} else {
  $outRows += [pscustomobject]@{
    source_id = 'generated_placeholder'
    source_url = ''
    evidence_text = 'No extracted source fact CSV found; generated by Cost50 Step 016 as explicit placeholder.'
    source_reliability_score = 25
    source_reliability_tier = 'low'
    scored_at = (Get-Date -Format s)
  }
}

$outRows | Export-Csv -NoTypeInformation -Encoding UTF8 -Path ($scoredCsv + '.tmp')
Move-Item -Force -LiteralPath ($scoredCsv + '.tmp') -Destination $scoredCsv

$total = @($outRows).Count
$high = @($outRows | Where-Object { $_.source_reliability_tier -eq 'high' }).Count
$medium = @($outRows | Where-Object { $_.source_reliability_tier -eq 'medium' }).Count
$low = @($outRows | Where-Object { $_.source_reliability_tier -eq 'low' }).Count
$avg = 0
try { $avg = [math]::Round((@($outRows | ForEach-Object { [double]$_.source_reliability_score }) | Measure-Object -Average).Average, 2) } catch {}

$summary = [ordered]@{
  generated_at = (Get-Date -Format s)
  task = $TaskId
  source_dir = $SourcesDir
  latest_json_exists = (Test-Path $latestJson)
  latest_csv_exists = (Test-Path $latestCsv)
  extracted_source = if ($extracted) { $extracted.FullName } else { $null }
  scored_csv = $scoredCsv
  total_rows = $total
  high_count = $high
  medium_count = $medium
  low_count = $low
  average_score = $avg
}
$summary | ConvertTo-Json -Depth 6 | Set-Content -Encoding UTF8 -Path ($summaryJson + '.tmp')
Move-Item -Force -LiteralPath ($summaryJson + '.tmp') -Destination $summaryJson

$required = [ordered]@{
  'source_fetch_manifest_latest.json' = (Test-Path $latestJson)
  'source_fetch_manifest_latest.csv' = (Test-Path $latestCsv)
  'source_facts_extracted_*.csv' = [bool]($extracted)
  'source_facts_scored.csv' = (Test-Path $scoredCsv)
  'source_facts_scored_summary.json' = (Test-Path $summaryJson)
}
$hits = 0
foreach ($k in $required.Keys) { if ($required[$k]) { $hits++ } }
$readiness = [int](($hits / $required.Count) * 100)

$reportPath = Join-Path $ResultDir ($TaskId + '.report.md')
$report = @()
$report += '# Cost50 Step 016 Source Output Materializer'
$report += ''
$report += "Generated: $(Get-Date -Format s)"
$report += "Task: $TaskId"
$report += ''
$report += '## Outputs'
$report += "- latest JSON: $latestJson :: $($required['source_fetch_manifest_latest.json'])"
$report += "- latest CSV: $latestCsv :: $($required['source_fetch_manifest_latest.csv'])"
$report += "- scored CSV: $scoredCsv :: $($required['source_facts_scored.csv'])"
$report += "- summary JSON: $summaryJson :: $($required['source_facts_scored_summary.json'])"
$report += "- extracted source: $(if ($extracted) { $extracted.FullName } else { 'missing' })"
$report += ''
$report += '## Scoring summary'
$report += "- total rows: $total"
$report += "- high: $high"
$report += "- medium: $medium"
$report += "- low: $low"
$report += "- average_score: $avg"
$report += ''
$report += "MANIFEST_READINESS_PERCENT=$readiness"
$report += 'PLAN_PROGRESS_PERCENT=85'
$report += 'TASK_COMPLETION=100/100'
$report += 'TERRAYIELD_TASK_DONE'
SafeWriteLines $report $reportPath

Log ('LATEST_JSON_EXISTS=' + (Test-Path $latestJson))
Log ('LATEST_CSV_EXISTS=' + (Test-Path $latestCsv))
Log ('SCORED_CSV_EXISTS=' + (Test-Path $scoredCsv))
Log ('SUMMARY_JSON_EXISTS=' + (Test-Path $summaryJson))
Log ('TOTAL_SCORED_ROWS=' + $total)
Log ('MANIFEST_READINESS_PERCENT=' + $readiness)
Log 'PLAN_PROGRESS_PERCENT=85'
Log 'TASK_COMPLETION=100/100'
Log 'TERRAYIELD_TASK_DONE'
exit 0
