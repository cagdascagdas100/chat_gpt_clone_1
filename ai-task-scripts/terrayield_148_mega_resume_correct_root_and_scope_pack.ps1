function Capture([scriptblock]$b){ try { return (& $b 2>&1 | Out-String) } catch { return ('ERROR: ' + $_.Exception.Message) } }
function SafeWrite([string]$rel,[string]$text){
  $full = [IO.Path]::GetFullPath((Join-Path $SecRoot $rel))
  $base = [IO.Path]::GetFullPath($SecRoot)
  if(-not $full.StartsWith($base,[StringComparison]::OrdinalIgnoreCase)){ throw ('scope violation: ' + $rel) }
  Ensure (Split-Path -Parent $full)
  Set-Content -LiteralPath $full -Encoding UTF8 -Value $text
  Say ('WROTE=security_accuracy_expansion/' + ($rel -replace '\\','/'))
}

